package com.string_assign;

public class Multiply {
	public static void main(String[] args) {
		

	   String m1 = "11";
	   String m2="2";
	   

	    int n1 = Integer.parseInt(m1);

	    int n2 = Integer.parseInt(m2);

	    int n3 = n1 * n2;

	    String t2 = String.format("%d", n3);
	    System.out.println(t2);
}
}

